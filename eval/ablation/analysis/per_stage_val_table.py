"""per_stage_val_table.py — Stage-wise best_val for every variant.

Greps each variant's train_a.log for the stage-end summary line:

    ★ stage-end ckpt: stage_<name>_done.pt  best_val=X.XXX

and produces a 4-column table (RIGID / PLANNER / PHYSICS / FULL).

Output:
  <out_dir>/stage_val.csv    ← raw table
  <out_dir>/stage_val.md     ← Tab S5 candidate (Markdown)

Usage::
    python eval/ablation/analysis/per_stage_val_table.py
"""
from __future__ import annotations

import argparse
import csv
import re
from pathlib import Path
from typing import Dict, Optional

# Match "★ stage-end ckpt: stage_<name>_done.pt  best_val=X.XXX"
STAGE_END_RE = re.compile(
    r"★\s+stage-end ckpt:\s+stage_(\w+)_done\.pt\s+best_val=([\d.eE+-]+|nan)"
)

STAGES = ["rigid", "planner", "physics", "full"]


def parse_stage_end(log_path: Path) -> Dict[str, float]:
    """Return {stage_name_lower: best_val_at_stage_end}."""
    if not log_path.exists():
        return {}
    out: Dict[str, float] = {}
    with open(log_path) as f:
        for line in f:
            m = STAGE_END_RE.search(line)
            if m:
                stage = m.group(1).lower()
                try:
                    out[stage] = float(m.group(2))
                except ValueError:
                    pass
    return out


def collect_logs(run_root: Path) -> Dict[str, Path]:
    """Discover {variant: log_path} under run_root."""
    out = {}
    if not run_root.exists():
        return out
    for variant_dir in sorted(run_root.iterdir()):
        if not variant_dir.is_dir() or variant_dir.name.startswith("_"):
            continue
        log = variant_dir / "seed_0" / "train_a.log"
        if log.exists():
            out[variant_dir.name] = log
    return out


def collect_main_logs(seeds=(0, 1, 2)) -> Dict[str, Path]:
    """Locate main_a per-seed logs."""
    out = {}
    for s in seeds:
        for log_name in ("train.log", "train_a.log"):
            for sub in (Path("runs/main_a"), Path("runs/main_exp")):
                p = sub / f"seed_{s}" / log_name
                if p.exists():
                    out[f"main_seed{s}"] = p
                    break
    return out


def fmt(v: Optional[float]) -> str:
    if v is None:
        return "—"
    return f"{v:.3f}"


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--module-root", type=str, default="runs/module")
    p.add_argument("--loss-root",   type=str, default="runs/loss")
    p.add_argument("--main-seeds",  nargs="+", type=int, default=[0, 1, 2])
    p.add_argument("--out-dir",     type=str, default="runs/_analysis")
    args = p.parse_args()

    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    # ── Gather everything ──
    sources: Dict[str, Path] = {}
    sources.update(collect_main_logs(args.main_seeds))
    sources.update(collect_logs(Path(args.module_root)))
    sources.update(collect_logs(Path(args.loss_root)))

    # ── Parse ──
    print(f"=== Parsing {len(sources)} logs for stage-end best_val ===")
    rows = {}
    for name, log in sorted(sources.items()):
        per_stage = parse_stage_end(log)
        rows[name] = per_stage
        found = "/".join(s for s in STAGES if s in per_stage)
        print(f"  {name:20s}  stages found: [{found}]  log={log}")

    # ── Write CSV ──
    csv_path = out_dir / "stage_val.csv"
    with open(csv_path, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["variant"] + STAGES + ["delta_full_vs_rigid"])
        for name in sorted(rows):
            r = rows[name]
            row = [name]
            for s in STAGES:
                row.append(f"{r[s]:.4f}" if s in r else "")
            if "rigid" in r and "full" in r:
                row.append(f"{r['full'] - r['rigid']:+.4f}")
            else:
                row.append("")
            w.writerow(row)
    print(f"\n  ✔ wrote {csv_path}")

    # ── Write Markdown ──
    md_path = out_dir / "stage_val.md"
    md = ["# Per-stage best validation loss\n",
          "Generated by `eval/ablation/analysis/per_stage_val_table.py`. "
          "Each cell is the `best_val_<stage>.pt` value as logged at stage end. "
          "`Δ vs main` compares the FULL-stage column with the mean of main_seed{0,1,2}.\n"]

    main_full_vals = [rows[k].get("full") for k in rows if k.startswith("main_seed")]
    main_full_vals = [v for v in main_full_vals if v is not None]
    main_full_mean = sum(main_full_vals) / len(main_full_vals) if main_full_vals else None

    md.append("| variant | RIGID | PLANNER | PHYSICS | FULL | Δ vs main FULL |")
    md.append("|---|---|---|---|---|---|")

    # Order: main first, then alphabetical
    main_keys = [k for k in rows if k.startswith("main_seed")]
    other_keys = [k for k in rows if not k.startswith("main_seed")]
    for name in sorted(main_keys) + sorted(other_keys):
        r = rows[name]
        cols = [fmt(r.get(s)) for s in STAGES]
        if main_full_mean is not None and "full" in r:
            d = r["full"] - main_full_mean
            cols.append(f"{d:+.3f} ({100*d/main_full_mean:+.1f}%)")
        else:
            cols.append("—")
        md.append("| " + name + " | " + " | ".join(cols) + " |")

    if main_full_mean is not None:
        md.append(f"\n*main FULL mean (over {len(main_full_vals)} seeds) = "
                  f"{main_full_mean:.3f}*")

    with open(md_path, "w") as f:
        f.write("\n".join(md) + "\n")
    print(f"  ✔ wrote {md_path}")


if __name__ == "__main__":
    main()
