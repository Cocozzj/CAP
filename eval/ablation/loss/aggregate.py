"""aggregate.py — combine Tier-2 (loss-term) ablation evals into a table.

Same logic as module/aggregate.py but reads only ``eval_a/`` (no B half).
Output: <out_dir>/table_loss.{csv,md}.
"""

from __future__ import annotations

import argparse
import csv
import json
import math
import sys
from pathlib import Path
from typing import Any, Dict, List, Optional

_THIS = Path(__file__).resolve().parent
if str(_THIS) not in sys.path:
    sys.path.insert(0, str(_THIS))
import variants as variants_mod   # noqa: E402


METRICS = [
    ("algebraic_gaps",     "closure_mean",       "Δ_clos"),
    ("algebraic_gaps",     "inverse_mean",       "Δ_inv"),
    ("algebraic_gaps",     "commutator_mean",    "Δ_comm"),
    ("trajectory_metrics", "ade_mean",           "ADE"),
    ("trajectory_metrics", "fde_mean",           "FDE"),
    ("success_rate",       "overall_mean",       "Success"),
    ("diversity",          "levenshtein_mean",   "Lev"),
    ("diversity",          "codebook_kl",        "Codebook-KL"),
]


def _read_json(path: Path) -> Optional[Dict[str, Any]]:
    if not path.exists():
        return None
    try:
        with open(path) as f:
            return json.load(f)
    except (json.JSONDecodeError, OSError):
        return None


def _walk(d: Optional[Dict[str, Any]], dotted: str) -> Optional[Any]:
    if d is None:
        return None
    cur: Any = d
    for k in dotted.split("."):
        if not isinstance(cur, dict) or k not in cur:
            return None
        cur = cur[k]
    return cur


def _fmt_num(x: float) -> str:
    if x is None or (isinstance(x, float) and math.isnan(x)):  return "-"
    if abs(x) < 1e-3 or abs(x) >= 1e4: return f"{x:.3e}"
    return f"{x:.4f}"


def _format(v: Any) -> str:
    if v is None: return "-"
    if isinstance(v, tuple) and len(v) == 2:
        m, s = v
        if m is None: return "-"
        return f"{_fmt_num(m)} ± {_fmt_num(s)}"
    if isinstance(v, (int, float)): return _fmt_num(v)
    return str(v)


def collect(eval_a_dir: Path) -> Dict[str, Any]:
    return {col: _walk(_read_json(eval_a_dir / sub / "results.json"), key)
            for sub, key, col in METRICS}


def _aggregate_seeds(seed_dirs: List[Path]) -> Dict[str, Any]:
    per_seed = [collect(d) for d in seed_dirs if d.exists()]
    if not per_seed:
        return {col: None for _, _, col in METRICS}
    out: Dict[str, Any] = {}
    for _, _, col in METRICS:
        vals = [r[col] for r in per_seed if isinstance(r.get(col), (int, float))]
        if not vals:                  out[col] = None
        elif len(vals) == 1:          out[col] = (vals[0], 0.0)
        else:
            mean = sum(vals) / len(vals)
            var  = sum((v - mean) ** 2 for v in vals) / (len(vals) - 1)
            out[col] = (mean, math.sqrt(var))
    return out


def build_rows(run_root: Path, main_root: Path, seed: int,
               variants: List[str], main_seeds: List[int] = (0, 1, 2),
               ) -> List[Dict[str, Any]]:
    rows: List[Dict[str, Any]] = []
    # main: aggregate over 3 seeds
    seed_dirs = [main_root / f"seed_{ms}" / "eval_a" for ms in main_seeds]
    existing  = [d for d in seed_dirs if d.exists()]
    if existing:
        rows.append({"variant": f"main (n={len(existing)})",
                     **_aggregate_seeds(existing)})
    # ablations: single seed
    for v in variants:
        rows.append({"variant": v,
                     **collect(run_root / v / f"seed_{seed}" / "eval_a")})
    return rows


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--run-root",  type=str, default="runs/loss")
    p.add_argument("--main-root", type=str, default="runs/loss/_main")
    p.add_argument("--seed",      type=int, default=0)
    p.add_argument("--variants",  nargs="+", default=variants_mod.list_variants())
    p.add_argument("--out-dir",   type=str, default="runs/loss/_aggregate")
    p.add_argument("--main-seeds", nargs="+", type=int, default=[0, 1, 2])
    args = p.parse_args()

    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    rows = build_rows(Path(args.run_root), Path(args.main_root),
                      args.seed, args.variants, args.main_seeds)

    cols = ["variant"] + [m[2] for m in METRICS]

    # CSV
    with open(out_dir / "table_loss.csv", "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(cols)
        for r in rows:
            w.writerow([_format(r.get(c)) for c in cols])

    # Markdown
    md_lines = [
        "# Tier-2 loss-term ablation",
        "",
        "Generated by `eval/ablation/loss/aggregate.py`. ",
        "Each row zero-outs ONE loss weight. `-` = eval JSON missing.",
        "",
        "| " + " | ".join(cols) + " |",
        "|" + "|".join(["---"] * len(cols)) + "|",
    ]
    for r in rows:
        md_lines.append("| " + " | ".join(_format(r.get(c)) for c in cols) + " |")
    md_lines += ["", "## Variants", ""]
    for v in variants_mod.list_variants():
        d = variants_mod.get_variant(v)["description"].splitlines()[0]
        md_lines.append(f"- **{v}** — {d}")
    (out_dir / "table_loss.md").write_text("\n".join(md_lines) + "\n")

    # Raw
    (out_dir / "raw.json").write_text(json.dumps(rows, indent=2, default=str))

    print(f"  ✔ wrote {out_dir / 'table_loss.csv'}")
    print(f"  ✔ wrote {out_dir / 'table_loss.md'}")
    print(f"  ✔ wrote {out_dir / 'raw.json'}  ({len(rows)} rows)")


if __name__ == "__main__":
    main()
